#Условие: 3.Пользователь вводит сумму в рублях, необходимо написать программу,
# которая переводит ее в доллары и выводит на экран.
# Все обработки сделать с помощью исключений.
#Формула Доллар = Рубль / Курс(88,17)
#------Решение-----#

try:
    ruble = float(input("Сколько рублей вы хотите поменять: "))
    courseDollar = 88.17
    convert = ruble / courseDollar
    if ruble < 0:
        raise ValueError("Cумма должна быть положительной")
except Exception as er:
    print(er)
else:
    print(f"Вы получите: {convert:.2f} доллара")

#-----Задача----#
#Условие: 4.	Напишите программу, которая запрашивает у пользователя ввод пароля, и выводит ошибку,
# если пароль длиной менее 8 символов, не содержит хотя бы одну цифру и одну букву.
# Если ввод некорректен, программа должна выводить сообщение "Некорректный пароль".
#------Решение-----#

password = input("Введите пароль: ")
if len(password) >= 8 and any(ch.isdigit() for ch in password) and any(ch.isalpha() for ch in password):
    print("Пароль успешно введен")
else:
    print("Некорректный пароль")
