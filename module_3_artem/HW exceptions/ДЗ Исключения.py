#Условие: Пользователь вводит числа, пока не введет stop и подсчитать сумму введенных чисел.
# Сделать обработку и выход из цикла с помощью исключений.
#------Решение-----#

number = 0
summ = 0
try:
    while number != 'stop':
        summ += int(number)
        number = input("Введите число: ")
except ValueError as error:
    print(f"Cтрока должна содержать число", error)
else:
    print(summ)


